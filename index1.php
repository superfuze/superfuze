                            <script>
                              let isDragging = false;
                              let startClientX = 0;
                              let currentIndex = 0;
                              let dragThreshold = 100; // Eşik değeri
                            
                              // Dotları bir kere oluştur
                              const dotsContainer = document.getElementById('sliderDots');
                              const totalSlides = document.querySelectorAll('.image-slider-images-item').length;
                            
                              for (let i = 0; i < totalSlides; i++) {
                                const dot = document.createElement('div');
                                dot.classList.add('slider-dot');
                                if (i === currentIndex) {
                                  dot.classList.add('active');
                                }
                                dot.onclick = () => showSlide(i);
                                dotsContainer.appendChild(dot);
                              }
                            
                              function showSlide(index) {
                                currentIndex = (index + totalSlides) % totalSlides;
                            
                                const items = document.querySelectorAll('.image-slider-images-item');
                                items.forEach(item => item.classList.remove('show'));
                                items[currentIndex].classList.add('show');
                            
                                // Dotları güncelle
                                const dots = dotsContainer.querySelectorAll('.slider-dot');
                                dots.forEach(dot => dot.classList.remove('active'));
                                dots[currentIndex].classList.add('active');
                              }
                            
                              function nextSlide() {
                                currentIndex = (currentIndex + 1) % totalSlides;
                                showSlide(currentIndex);
                              }
                            
                              function prevSlide() {
                                currentIndex = (currentIndex - 1 + totalSlides) % totalSlides;
                                showSlide(currentIndex);
                              }
                            
                              function startDragging(event) {
                                isDragging = true;
                                startClientX = event.touches[0].clientX;
                              }
                            
                              function handleDragging(event) {
                                if (isDragging) {
                                  const distance = event.touches[0].clientX - startClientX;
                                  const slidesCount = document.querySelectorAll('.image-slider-images-item').length;
                                  const dragIndex = currentIndex - Math.round(distance / document.body.clientWidth * slidesCount);
                            
                                  if (Math.abs(distance) > dragThreshold) {
                                    showSlide(dragIndex);
                                    stopDragging();
                                  }
                                }
                              }
                            
                              function stopDragging() {
                                isDragging = false;
                              }
